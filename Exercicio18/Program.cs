//laço que conta de 100 a 200
for (int contador = 100; contador <= 200; contador++)
{
    //condição que verifica se o numero dividido por 2 é diferente de zero
    if (contador % 2 != 0)
    {
        //imprime na tela o valor do numero impar
        Console.WriteLine(contador);
    }
}

Console.ReadLine();
